#include <stdio.h>
int main(){
    int nusp = 11295758;
    printf("Imprimindo inteiro: %i\n", nusp);
    printf("Imprimindo numero: %d\n", nusp);
    printf("Imprimindo float (com cast): %f\n", (float)nusp);
    printf("Imprimindo quociente: %i\n", nusp / 2);
    printf("Imprimindo resto: %d\n", nusp % 2);
    return 0;
}